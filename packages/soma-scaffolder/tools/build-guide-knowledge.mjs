#!/usr/bin/env node
// Compile the Soma-apps knowledge base + the app-spec schema into a knowledge.js
// (window.SiteKnowledge) for the meta-app's guide ("Ada"), so it can explain what a
// Soma app is, walk the affordance menu, lay out the choices, and know the exact
// fields a spec needs. One source (docs/soma-apps + schema) → the guide's grounding.
//
//   node tools/build-guide-knowledge.mjs [outFile]    # default: ./knowledge.guide.js

import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const KB_DIR = join(__dirname, "..", "..", "..", "docs", "soma-apps");
const SCHEMA = join(__dirname, "..", "schema", "soma-app.schema.json");
const DOCS = ["README.md", "AFFORDANCES.md", "APP-SPEC.md", "BUILD-MODEL.md"];

// Split a markdown doc into { title, sections:[{heading, text}] } by ## headings.
function parseDoc(md) {
  const lines = md.split("\n");
  let title = "";
  const sections = [];
  let cur = null;
  for (const line of lines) {
    if (!title && line.startsWith("# ")) { title = line.slice(2).trim(); continue; }
    if (line.startsWith("## ")) {
      if (cur) sections.push(cur);
      cur = { heading: line.slice(3).trim(), text: "" };
    } else if (cur) {
      cur.text += line + "\n";
    }
  }
  if (cur) sections.push(cur);
  return { title, sections: sections.map((s) => ({ heading: s.heading, text: s.text.trim() })) };
}

// Pull each affordance + its config field names from the JSON Schema, so the guide
// can enumerate the choices precisely.
function affordancesFromSchema(schema) {
  const aff = schema?.properties?.soma_app?.properties?.affordances?.properties || {};
  return Object.entries(aff).map(([name, def]) => ({
    name,
    fields: Object.keys(def.properties || {}).filter((f) => f !== "enabled"),
  }));
}

const docs = DOCS.map((f) => {
  const parsed = parseDoc(readFileSync(join(KB_DIR, f), "utf8"));
  return { doc: f, title: parsed.title, sections: parsed.sections };
});

const schema = JSON.parse(readFileSync(SCHEMA, "utf8"));

const overview = docs[0].sections.find((s) => /what a soma app is/i.test(s.heading))?.text
  || docs[0].sections[0]?.text || "";

const knowledge = {
  generatedAt: new Date().toISOString().slice(0, 10),
  source: "soma-platform/docs/soma-apps + schema/soma-app.schema.json",
  overview,
  docs,
  affordances: affordancesFromSchema(schema),
  specVersion: schema?.properties?.soma_app?.properties?.meta?.properties?.spec_version || null,
};

const out = process.argv[2] || join(process.cwd(), "knowledge.guide.js");
const body =
  "// AUTO-GENERATED by build-guide-knowledge.mjs from soma-platform/docs/soma-apps.\n" +
  "// Do not hand-edit; regenerate when the knowledge base changes.\n" +
  "window.SiteKnowledge = " + JSON.stringify(knowledge, null, 2) + ";\n";
writeFileSync(out, body);

const total = docs.reduce((n, d) => n + d.sections.length, 0);
console.log(`✓ guide knowledge → ${out}`);
console.log(`  ${docs.length} docs, ${total} sections, ${knowledge.affordances.length} affordances`);
